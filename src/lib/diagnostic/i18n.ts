function buildGenericCopy(locale: WebsiteLocale): DiagnosticCopy {
  const profile = websiteLocaleProfiles[locale];
  const nativeName = genericLocaleNames[locale as keyof typeof genericLocaleNames] ?? profile.nativeName;
  return {
    ...en,
    locale,
    dir: websiteLocaleDirection[locale],
    languageName: profile.name,
    nativeLanguageName: nativeName,
    languageInstruction: languageInstructions[locale],
    intro: {
      ...en.intro,
      eyebrow: `${nativeName} · 10 min`,
      title: `Sundae Operations Diagnostic (${nativeName})`,
      body:
        "This diagnostic uses your selected language for generated recommendations, email, and the shareable report. Product names remain unchanged for consistency.",
      start: "Start diagnostic",
    },
    generating: {
      ...en.generating,
      footer: `Generated in ${nativeName} by Sundae's diagnostic engine`,
    },
  };
}

function mergeCopy(locale: WebsiteLocale): DiagnosticCopy {
  const base = locale === "en" ? en : buildGenericCopy(locale);
  const override = overrides[locale];
  if (!override) return base;
  return {
    ...base,
    ...override,
    locale,
    dir: websiteLocaleDirection[locale],
    languageName: websiteLocaleProfiles[locale].name,
    nativeLanguageName: websiteLocaleProfiles[locale].nativeName,
    intro: { ...base.intro, ...override.intro },
    dimensions: { ...base.dimensions, ...override.dimensions },
    flow: { ...base.flow, ...override.flow },
    generating: { ...base.generating, ...override.generating },
    report: { ...base.report, ...override.report },
    share: { ...base.share, ...override.share },
  };
}

export function getDiagnosticCopy(locale?: string | null): DiagnosticCopy {
  return mergeCopy(normalizeWebsiteLocale(locale));
}

export function getDiagnosticPromptInstruction(locale?: string | null): string {
  const copy = getDiagnosticCopy(locale);
  return [
    `Target language: ${copy.languageInstruction}.`,
    `Write every user-visible value in the DiagnosticReport JSON in ${copy.languageInstruction}.`,
    "Keep JSON keys and enum values exactly as the schema defines them.",
    `Preserve these glossary/product names exactly: ${productGlossary}.`,
    "Use native, polished operator-facing language. Do not produce literal or fallback-style translations.",
    "Do not mention the model, provider, translation process, or language instruction.",
  ].join("\n");
}

export function withDiagnosticLocale(url: string, locale?: string | null): string {
  const normalized = normalizeWebsiteLocale(locale);
  const separator = url.includes("?") ? "&" : "?";
  return `${url}${separator}locale=${encodeURIComponent(normalized)}`;
}
